
 const cal = {
    add : function(a,b) {
        let sum = a + b;
        return sum;
    },
    sub : function (a,b) {
        let sub = a-b;
        return sub;
    },
    mul : function (a,b) {
        let mul = a*b;
        return mul;
    },
    div : function (a,b) {
        let div = a/b;
        return div;
    }
}
export default cal;